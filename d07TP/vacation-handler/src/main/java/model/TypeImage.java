package model;

/**
 *
 * @author isi
 */
public class TypeImage {
    private int id;
    private String nom;

    public TypeImage() {
    }

    public TypeImage(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
